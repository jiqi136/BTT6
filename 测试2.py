
import re  # 正则式  [\u4e00-\u9fa5]{0,}$
短标题="[复仇者联盟3.无限战争][3.58G][HEVC 10bit 1080P][内封中英双字][麻辣江湖压制组]"

符号列表 = ['/', '.', ' ']
for 符号 in 符号列表:
    短标题 =短标题.strip(符号)  # 默认则是去除空格
    if 符号 in 短标题:
        if 符号 == ' ':
            符号 = '\s'
        if 符号 == '.':
            符号 = '\.'
        规则 = '{}'.format(符号)+'.{0,}'  # 不换行 end=""
        短标题 = re.sub(规则, '',短标题, count=0)

print(短标题)